# mysite/asgi.py
import os

#AuthMiddlewareStack will populate the connectionâ€™s scope with a reference to the 
#currently authenticated user
from channels.auth import AuthMiddlewareStack
#URLrouter will examine the HTTP path of the connection to route it to a particular 
#consumer, based on the provided url patterns.
from channels.routing import ProtocolTypeRouter, URLRouter
from channels.security.websocket import AllowedHostsOriginValidator
from django.core.asgi import get_asgi_application

os.environ.setdefault("DJANGO_SETTINGS_MODULE", "mysite.settings")
# Initialize Django ASGI application early to ensure the AppRegistry
# is populated before importing code that may import ORM models.
django_asgi_app = get_asgi_application()

import chat.routing

application = ProtocolTypeRouter(
    {
        "http": django_asgi_app,
        "websocket": AllowedHostsOriginValidator(
            AuthMiddlewareStack(URLRouter(chat.routing.websocket_urlpatterns))
        ),
    }
)